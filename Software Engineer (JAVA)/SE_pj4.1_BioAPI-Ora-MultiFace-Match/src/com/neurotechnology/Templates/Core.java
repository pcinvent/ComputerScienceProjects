package com.neurotechnology.Templates;

public class Core {

		private int X; ///< X coordinate of this #NFCore.
		private int Y; ///< Y coordinate of this #NFCore.
		private int Angle; ///< Angle of this #NFCore.
		
		public void setX(int x) {
			X = x;
		}
		public int getX() {
			return X;
		}
		public void setY(int y) {
			Y = y;
		}
		public int getY() {
			return Y;
		}
		public void setAngle(int angle) {
			Angle = angle;
		}
		public int getAngle() {
			return Angle;
		}

}
