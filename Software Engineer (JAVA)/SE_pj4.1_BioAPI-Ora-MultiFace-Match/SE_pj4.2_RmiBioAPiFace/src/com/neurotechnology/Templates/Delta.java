package com.neurotechnology.Templates;

public class Delta {

		private int X; ///< X coordinate of this #NFDelta.
		private int Y; ///< Y coordinate of this #NFDelta.
		private int Angle1; ///< First angle of this #NFDelta.
		private int Angle2; ///< Second angle of this #NFDelta.
		private int Angle3; ///< Third angle of this #NFDelta.
		
		public void setX(int x) {
			X = x;
		}
		public int getX() {
			return X;
		}
		public void setY(int y) {
			Y = y;
		}
		public int getY() {
			return Y;
		}
		public void setAngle1(int angle1) {
			Angle1 = angle1;
		}
		public int getAngle1() {
			return Angle1;
		}
		public void setAngle2(int angle2) {
			Angle2 = angle2;
		}
		public int getAngle2() {
			return Angle2;
		}
		public void setAngle3(int angle3) {
			Angle3 = angle3;
		}
		public int getAngle3() {
			return Angle3;
		}

}
